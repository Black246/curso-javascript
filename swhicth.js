// switch
let nota = 8;

switch (nota) {
    case 5:
        console.log("nota sobresaliente");
        break;
    case 4:
        console.log("Nota alta");
        break;
    case 3: 
        console.log("Nota Basica");
        break;
    case 2:
        console.log("No has aprobado");
        break;
    case 1:
        console.log("No has estudiado nada");
        break;
    default: 
        ("Error");
        break;
}